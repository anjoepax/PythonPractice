"""
More into slicing and indexing
"""
str_sample = "This is a sample string"
a = str_sample[:]
print(a)
a = str_sample[1:]
print(a)
a = str_sample[:6]
print(a)
a = str_sample[-1]
print(a)
a = str_sample[:-1]
print(a)
a = str_sample[::3]
print(a)

# Reverse a string
a = str_sample[::-1]
print(a)
print(a)
