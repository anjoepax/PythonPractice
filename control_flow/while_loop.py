"""
While Loop in Python
"""
x = 0
while x <= 10:
    print("Value of x is " + str(x))
    x = x + 1

l = []
num = 0
while num < 10:
    l.append(num)
    num += 1
print(l)

words = ['aba', 'xyz', 'xgx', 'dssd', 'sdjh']
for word in words:
    print(word)


s_example = "Hello World"
for c in s_example:
    print(c)
